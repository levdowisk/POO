
public class professor {
    private String nomeP; 
    private int numeroP;
    
    public professor(String nomeP, int numeroP) {
        this.nomeP = nomeP;
        this.numeroP = numeroP;
    }
    
    public int getNumeroP() {
        return numeroP;
    }

    public String getNomeP() {
        return nomeP;
    }
}